def double_letter(words):
  print(f"\nThe word you entered is: {words}\n")
  number_letter = len(words)
  for i in range(number_letter - 1):
    if words[i] == words[i+1]:
      return 'yes'
  return 'no'

def compression(word):
  print(f"\nThe string you entered is: {word}\n")
  word = word.upper()
  number_letter = len(word)
  out = ''
  prev_letter = word[0]
  count = 1
  for i in range(1, number_letter):
    if word[i] == prev_letter:
      count += 1
    else:
      if not out == '':
        out += ' '
      out += f"{count}{prev_letter}"
      prev_letter = word[i]
      count = 1
  if not out == '':
    out += ' '
  out += f"{count}{prev_letter}"

  return out


